    public function run(): void
    {
        $categoryMap = $this->loadCategoryMap();
        $articles = $this->getData();

        $articleStmt = $this->pdo->prepare(
            'INSERT INTO articles (image, title, description, body, views, created_at)
             VALUES (:image, :title, :description, :body, :views, :created_at)'
        );

        $pivotStmt = $this->pdo->prepare(
            'INSERT INTO article_categories (article_id, category_id)
             VALUES (:article_id, :category_id)'
        );

        foreach ($articles as $index => $article) {
            $articleStmt->bindValue('image', $this->generateImageUrl($index + 1));
            $articleStmt->bindValue('title', $article['title']);
            $articleStmt->bindValue('description', $article['description']);
            $articleStmt->bindValue('body', $article['body']);
            $articleStmt->bindValue('views', rand(5, 500), PDO::PARAM_INT);
            $articleStmt->bindValue('created_at', $this->generateRandomDate());
            $articleStmt->execute();

            $articleId = (int) $this->pdo->lastInsertId();

            foreach ($article['categories'] as $categoryName) {
                if (!isset($categoryMap[$categoryName])) {
                    throw new \RuntimeException("Unknown category: {$categoryName}");
                }
                $pivotStmt->bindValue('article_id', $articleId, PDO::PARAM_INT);
                $pivotStmt->bindValue('category_id', $categoryMap[$categoryName], PDO::PARAM_INT);
                $pivotStmt->execute();
            }
        }

        echo "Created " . count($articles) . " articles with category assignments.\n";
    }

    /** @return array<string, int> category name => id */
    private function loadCategoryMap(): array
    {
        $stmt = $this->pdo->query('SELECT id, name FROM categories');
        $map = [];

        while ($row = $stmt->fetch()) {
            $map[$row['name']] = (int) $row['id'];
        }

        return $map;
    }

    private function generateImageUrl(int $number): string
    {
        return "https://picsum.photos/800/400?random={$number}";
    }

    private function generateRandomDate(): string
    {
        $daysAgo = rand(1, 90);

        return date('Y-m-d H:i:s', strtotime("-{$daysAgo} days"));
    }

